def create_excel_file():
    """–°–æ–∑–¥–∞–µ–º Excel —Ñ–∞–π–ª —Å –ª–∏—Ü–µ–Ω–∑–∏–æ–Ω–Ω—ã–º–∏ –∏–≥—Ä–æ–∫–∞–º–∏"""
    
    # –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—ã–π workbook
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "–õ–∏—Ü–µ–Ω–∑–∏–æ–Ω–Ω—ã–µ –∏–≥—Ä–æ–∫–∏ 2025"
    
    # –ó–∞–≥–æ–ª–æ–≤–∫–∏ –∫–æ–ª–æ–Ω–æ–∫
    headers = ["‚Ññ –ø/–ø", "–§–ò–û", "–î–∞—Ç–∞", "‚Ññ –ª–∏—Ü–µ–Ω–∑–∏–∏", "–ì–æ—Ä–æ–¥"]
    
    # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–∫–∏
    for col, header in enumerate(headers, 1):
        ws.cell(row=1, column=col, value=header)
    
    # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –∏–≥—Ä–æ–∫–æ–≤
    for row_num, player_data in enumerate(players_data, 2):  # –ù–∞—á–∏–Ω–∞–µ–º —Å–æ 2-–π —Å—Ç—Ä–æ–∫–∏
        for col, value in enumerate(player_data, 1):
            ws.cell(row=row_num, column=col, value=value)
    
    # –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º —à–∏—Ä–∏–Ω—É –∫–æ–ª–æ–Ω–æ–∫ –¥–ª—è –ª—É—á—à–µ–≥–æ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è
    column_widths = [8, 25, 12, 12, 20]  # ‚Ññ –ø/–ø, –§–ò–û, –î–∞—Ç–∞, ‚Ññ –ª–∏—Ü–µ–Ω–∑–∏–∏, –ì–æ—Ä–æ–¥
    for i, width in enumerate(column_widths, 1):
        ws.column_dimensions[openpyxl.utils.get_column_letter(i)].width = width
    
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Å—Ç–∏–ª–∏ –¥–ª—è –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
    from openpyxl.styles import Font, PatternFill, Alignment
    
    header_font = Font(bold=True)
    header_fill = PatternFill(start_color="E6F3FF", end_color="E6F3FF", fill_type="solid")
    center_alignment = Alignment(horizontal="center", vertical="center")
    
    for col in range(1, len(headers) + 1):
        header_cell = ws.cell(row=1, column=col)
        header_cell.font = header_font
        header_cell.fill = header_fill
        header_cell.alignment = center_alignment
    
    # –¶–µ–Ω—Ç—Ä–∏—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ –≤ –∫–æ–ª–æ–Ω–∫–∞—Ö ‚Ññ –ø/–ø, –î–∞—Ç–∞ –∏ ‚Ññ –ª–∏—Ü–µ–Ω–∑–∏–∏
    for row in range(2, len(players_data) + 2):
        ws.cell(row=row, column=1).alignment = center_alignment  # ‚Ññ –ø/–ø
        ws.cell(row=row, column=3).alignment = center_alignment  # –î–∞—Ç–∞
        ws.cell(row=row, column=4).alignment = center_alignment  # ‚Ññ –ª–∏—Ü–µ–Ω–∑–∏–∏
    
    # –î–æ–±–∞–≤–ª—è–µ–º –≥—Ä–∞–Ω–∏—Ü—ã –¥–ª—è —Ç–∞–±–ª–∏—Ü—ã
    from openpyxl.styles import Border, Side
    
    thin_border = Border(
        left=Side(style='thin'),
        right=Side(style='thin'),
        top=Side(style='thin'),
        bottom=Side(style='thin')
    )
    
    for row in range(1, len(players_data) + 2):
        for col in range(1, len(headers) + 1):
            ws.cell(row=row, column=col).border = thin_border
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º —Ñ–∞–π–ª
    filename = "/Users/afrikanova/projects/petanque/rate-scoring/–õ–∏—Ü–µ–Ω–∑–∏–æ–Ω–Ω—ã–µ_–∏–≥—Ä–æ–∫–∏_2025.xlsx"
    wb.save(filename)
    
    print(f"‚úÖ Excel —Ñ–∞–π–ª —Å–æ–∑–¥–∞–Ω: {filename}")
    print(f"üìä –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ª–∏—Ü–µ–Ω–∑–∏–æ–Ω–Ω—ã—Ö –∏–≥—Ä–æ–∫–æ–≤: {len(players_data)}")
    
    # –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º –∏–≥—Ä–æ–∫–æ–≤ –ø–æ –≥–æ—Ä–æ–¥–∞–º
    cities = {}
    for player in players_data:
        city = player[4]  # –ì–æ—Ä–æ–¥ –≤ 5-–π –∫–æ–ª–æ–Ω–∫–µ (–∏–Ω–¥–µ–∫—Å 4)
        cities[city] = cities.get(city, 0) + 1
    
    print("\nüèôÔ∏è –†–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –ø–æ –≥–æ—Ä–æ–¥–∞–º:")
    for city, count in sorted(cities.items(), key=lambda x: x[1], reverse=True):
        print(f"  {city}: {count} –∏–≥—Ä–æ–∫(–æ–≤)")
    
    return filename


